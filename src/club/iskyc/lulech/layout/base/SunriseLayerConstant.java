package club.iskyc.lulech.layout.base;

/**
 * Constant value for layer size.
 *
 * @author lulec
 * @since 1.0
 * */
public interface SunriseLayerConstant {
    // Static Parameters For Layers
    /**
     * This is the outer width of the main frame{@code basicLayer}.
     * */
    int outerWidth = 400;

    /**
     * This is the outer height of the main frame{@code basicLayer}.
     * */
    int  outerHeight = 400;

    /**
     * This is the default border width for any layer.
     * */
    int basicBorderWidth = 1;

    /**
     * This is basic start x position for frame on the hole desktop.
     * */
    int layingStartX = 100;

    /**
     * This is basic start y position for frame on the hole desktop.
     * */
    int layingStartY = 100;

    /**
     * This is the outer height of the main frame{@code formPanelLayer}.
     * */
    int formLayerHeight = 200;
}
