package club.iskyc.lulech.layout.util;

/**
 * Some constants values for those needed to use.
 *
 * @author lulec
 * @since 1.0
 * */
public interface BaseConstantValue {
    /**
     * Regex string package name splitter {@code .}.
     * */
    String regPackageSplitter = "\\.";

    /**
     * String package name splitter {@code .}.
     * */
    String packageSplitter =  ".";

    /**
     * Regex string Linux file path splitter {@code /}.
     * */
    String regLinuxSplitter = "/";

    /**
     * String Linux file path splitter {@code /}.
     * */
    String linuxSplitter = "/";

    /**
     * Regex string Windows file path splitter {@code \}.
     * */
    String regWindowsSplitter = "\\\\";

    /**
     * String Windows file path splitter {@code \}.
     * */
    String windowsSplitter = "\\";
}
